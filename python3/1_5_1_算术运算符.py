'''
+	加 - 两个对象相加
-	减 - 得到负数或是一个数减去另一个数
*	乘 - 两个数相乘或是返回一个被重复若干次的字符串
/	除 - x 除以 y
%	取模 - 返回除法的余数
**	幂 - 返回x的y次幂
//	取整除 - 向下取接近除数的整数
'''
a = 21
b = 10
c = 2

print('a:', a, 'b:', b, 'c:',c)
print('a + b 加：', a + b)
print('a - b 减：', a - b)
print('a * b 乘：', a * b)
print('a / b 除：', a / b)
print('b % c 取模：', b % c)
print('a ** c 幂：', a ** c)
print('a // c 幂：', a // c)
